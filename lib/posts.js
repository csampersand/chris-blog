import fs from 'fs'
import path from 'path'
import matter from 'gray-matter'
import remark from 'remark'
import html from 'remark-html'

const postsDirectory = path.join(process.cwd(), 'posts')

const asyncFilter = async (arr, predicate) =>
  Promise.all(arr.map(predicate)).then((results) =>
    arr.filter((_v, index) => results[index])
  )

export async function getSortedPostsDataByTag(tag) {
  // Get file names under /posts
  const fileNames = fs.readdirSync(postsDirectory)
  const filteredPostData = await asyncFilter(fileNames, async (fileName) => {
    const id = fileName.replace(/\.md$/, '')
    const postData = await getPostData(id)
    return postData.tags && postData.tags.includes(tag)
  })
  const formattedPostData = filteredPostData.map((fileName) => {
    // Remove ".md" from file name to get id
    const id = fileName.replace(/\.md$/, '')

    // Read markdown file as string
    const fullPath = path.join(postsDirectory, fileName)
    const fileContents = fs.readFileSync(fullPath, 'utf8')

    // Use gray-matter to parse the post metadata section
    const matterResult = matter(fileContents)

    // Combine the data with the id
    return {
      id,
      ...matterResult.data,
    }
  })
  // Sort posts by date
  return formattedPostData.sort((a, b) => {
    if (a.date < b.date) {
      return 1
    } else {
      return -1
    }
  })
}

